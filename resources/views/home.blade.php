                                        <h3><a href="mat-ong-rung-va-mat-ong-nuoi-loai-nao-tot-hon.html" title="Mật ong rừng v&#224; mật ong nu&#244;i loại n&#224;o tốt hơn?" class="a-title">Mật ong rừng v&#224; mật ong nu&#244;i loại n&#224;o tốt hơn?</a></h3>

                                        <div class="summary_blog">
                                            <p>
                                                Mật ong rừng được lấy từ những tổ ong sống dã sinh trong rừng. Khi lấy mật, người ta sẽ phá luôn tổ ong. Mật ong nuôi được thu hoạch từ những thùng nuôi ong. Khi lấy mật, người ta vẫn giữ lại tổ cho ong. Hai loại này loại nào tốt hơn?
                                                Mật ong rừng&nbsp;
                                                Để có mật ong rừng, người ta có 2 cách khác nhau:
                                                Cách thứ nhất – hái lượm:
                                                Người săn mật tìm kiếm trong các cánh rừng các tổ ong có mật trên cành cây hay trong hốc đá… Họ hun khói cho ong bay đi để lấy cả tổ xuống, sau đó vắt mật bằng tay.
                                                Đây là cách mật ong nguyên sơ nhất. Mật ong lẫn rất nhiều tạp chất và xác của ấu trùng ong non. Vì là hái lượm nên cứ thấy là lấy, cho dù mật ong vẫn còn non. Mật còn non, chứa nhiều nước, nhanh bị lên men chua. Các can mật ong rừng thường phình to. Khi mở nút sẽ có hiện tượng bật khí ga.&nbsp;
                                                Cách thứ hai – gác kèo:
                                                Người U Minh Hạ “gác kèo” để dụ ong rừng đến làm tổ. Người ta tạo ra các chạc cây (gọi là kèo) giống như các chạc cây ong thường làm tổ. Họ gác chúng lên cây, tại vị trí gần nguồn hoa, nơi có nhiều ong. Ong sẽ dến làm tổ. Người gác kèo đợi đến khi có mật thì quay lại thu hoạch. Cách này khá hơn cách hái lượm. Người gác kèo tính toán thời gian quay lại để lấy mật lúc mật đã bớt non. Sau đó, cũng vắt mật thủ công.
                                                Gác kèo gần giống với nuôi ong. Điểm khác là người ta không làm tổ cho ong bằng các thùng gỗ và không nhân giống ong.
                                                Mật ong nuôi
                                                Nuôi ong:
                                                Đầu tiên, người nuôi ong làm “nhà” cho ong. Họ đóng các thùng gỗ có nặp đậy bên trên, Bên trong xếp khoảng 6 đến 10 cầu ong bằng khung gỗ. Mỗi khung đều đúc sẵn các lỗ tổ cao khoảng 1/3 so với lỗ tổ hoàn thiện bằng chính sáp ong. Ong sẽ tự tiết sáp tiếp tục xây tổ cho đến khi hoàn thiện.
                                                Gọi là nuôi, nhưng thực chất, người nuôi chỉ quản lý mật thu được. Đàn ong tự nuôi sống bằng khả năng làm mật hoang dã. Chỉ khi đàn ong đông, người ta tạo ong chúa mới để tách đàn. Khi mùa hoa hết, người nuôi ong cho ong ăn thức ăn bổ sung bao gồm: phấn hoa, bột đầu nành và đường để dưỡng ong mà không thu mật.
                                                Người nuôi ong sẽ chỉ khai thác mật khi mùa hoa nở rộ. Mật thu được từ lần quay mật thứ nhất và thậm chí lần thứ 2 sẽ dùng làm thức ăn chăn nuôi. Mật ong chất lượng sẽ được khai thác từ vòng thứ 3 trở đi. Đến mùa hoa tàn, ong phải dưỡng, lúc đó cũng không khai thác mật. Việc khai thác đúng vào mùa hoa nở rộ sẽ giúp đảm bảo mật ong đạt chât lượng cao.
                                                Đàn ong thường xuyên được di chuyển. Khi cánh rừng này hết hoa, ong sẽ được đưa đến các cánh rừng khác. Việc di chuyển đàn ong giúp ong không phải bay xa. Những trại ong thường có hàng trăm đàn.
                                                Quay mật&nbsp;
                                                Họ thường thu hoạch mật cùng một lúc bằng cách quay mật. Các cầu ong được cắt miệng lỗ tổ chứa mật, xếp trong thùng quay mật. Khi trục của thùng quay, mật trong các lỗ tổ văng ra theo lực ly tâm. Mật chảy xuống đáy thùng, nơi có vòi rót mật vào can chứa.
                                                Cách thu hoạch này vệ sinh hơn và rất ít khi bị lẫn các ấu trung ong non. Sau khi quay mật xong, người ta trả lại “nhà” cho đàn ong. Đây là cách lấy mật mà không phá tổ ong.
