                                                Trong rừng có nhiều loại hoa. Nên mật ong rừng thường là mật đa hoa. Mật đa hoa phong phú hơn về các loại vi chất nên tốt hơn mật đơn hoa. Nhưng mật ong rừng thường bị lấy non. Mật ong nuôi thường là mật đơn hoa nhưng lại có thể thu hoạch chín. Hương vị của mật đơn hoa và đa hoa được so sánh, đánh giá phụ thuộc vào gu của người dùng. Người Việt thích mật đa hoa vì đây là đặc trưng của mật ong rừng. Người Âu Mỹ lại thích mật ong đơn hoa. Họ coi đó là đặc sản.
